    private void buttonAddFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddFuncionarioActionPerformed
        try{
            if (txtSalario.getText().isBlank()) {
                JOptionPane.showMessageDialog(this, "Salario deve ser informado");
            }
        if (jrbComum.isSelected()) {
            empresa.incluirFuncionario(new Funcionario(txtNomeFuncionario.getText(), Double.parseDouble(txtSalario.getText())));
        } else if (jrbProgramador.isSelected()) {
            Programador programador = new Programador(txtNomeFuncionario.getText(), Double.parseDouble(txtSalario.getText()));
            empresa.incluirFuncionario(programador);
            TelaProgramador tela = new TelaProgramador(programador);
            tela.setVisible(true);
        } else {
            String nome = txtNomeFuncionario.getText();
            double salario = Double.parseDouble(txtSalario.getText());
            TelaVendedor telaVendedor = new TelaVendedor(nome, salario, empresa);
            telaVendedor.setVisible(true);
        }
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_buttonAddFuncionarioActionPerformed

