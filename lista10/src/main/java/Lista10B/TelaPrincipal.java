    private void buttonListarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonListarDadosActionPerformed
        String dados = "";
        
        for (Funcionario f: empresa.getFuncionarios()) {
            dados += f.getNome() + " R$" + f.getSalarioBase() + "\n";
            if (f instanceof Programador) {
                Programador p = (Programador)f;
                for (String s: p.getLinguagens()) {
                    dados += s + " - ";
                }
                dados += "\n";
            } else if (f instanceof Vendedor) {
                Vendedor v = (Vendedor)f;
                dados += "Comiss√£o: " + v.getPercentualComissao() + "\n";
                for (Venda venda: v.getVendas()) {
                    dados += venda.getValor() + " - ";
                }
                dados += "\n";
            }
            dados += "Sal√°rio: " + f.calcularSalario() + "\n";
        }
        
        JOptionPane.showMessageDialog(this, dados);
    }//GEN-LAST:event_buttonListarDadosActionPerformed

