    private void buttonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalcularActionPerformed
        String strAltura = txtAltura.getText();
        String strComprimento = txtComprimento.getText();
        
        int altura = Integer.parseInt(strAltura);
        int comprimento = Integer.parseInt(strComprimento);
        
        int resultadoPerimetro = (2*altura) + (2*comprimento); 
        int resultadoArea = altura*comprimento;
        
        String strResultadoPerimetro = String.valueOf(resultadoPerimetro);
        txtPerimetro.setText(strResultadoPerimetro);
        String strResultadoArea = String.valueOf(resultadoArea);
        txtArea.setText(strResultadoArea);
    }//GEN-LAST:event_buttonCalcularActionPerformed

